# DESAFIO 2: MATRIZES MUSICAIS — TRANSPOSIÇÃO

# Aqui a ideia é pegar matrizes 2x2 de frequências musicais

# e transpor elas (linha vira coluna e coluna vira linha).

# A transposição de [[a, b], [c, d]] vira [[a, c], [b, d]].

# -----------------------------------------------------------------------

print("\n" + "=" * 55)

print(" DESAFIO 2 — TRANSPOSIÇÃO DAS MATRIZES MUSICAIS")

