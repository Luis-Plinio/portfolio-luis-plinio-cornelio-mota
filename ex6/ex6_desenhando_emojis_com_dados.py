print("\n" + "=" * 55)
print(" DESAFIO 3 — PLAYLIST DE IMAGENS (INTEGRADOR)")
print("=" * 55)

# A estrutura: um dicionário chamado "playlist" que contém:

# - "titulo" (string): nome da playlist

# - "frames" (lista): lista de frames/cenas da animação

# - Cada frame é um dicionário com "id", "duracao" (tupla) e "pixels" (lista de tuplas)

playlist = {
  "titulo": "Animação Emoji Pisca",
  "frames": [
    {
      "id": 1,
      "duracao": (0, 500), # tupla (inicio_ms, fim_ms)
      "pixels": [
        (255, 255, 0), # pixel 0 — rosto amarelo
        (0, 0, 0),   # pixel 1 — olho esquerdo (aberto)
        (255, 255, 0), # pixel 2 — nariz
        (0, 0, 0),   # pixel 3 — olho direito (aberto)
        (255, 255, 0)  # pixel 4 — boca
      ]
    },
    {
      "id": 2,
      "duracao": (500, 1000),
      "pixels": [
        (255, 255, 0), # pixel 0 — rosto amarelo
        (255, 255, 0), # pixel 1 — olho esquerdo (fechado = amarelo)
        (255, 255, 0), # pixel 2 — nariz
        (255, 255, 0), # pixel 3 — olho direito (fechado = amarelo)
        (255, 255, 0)  # pixel 4 — boca
      ]
    },
    {
      "id": 3,
      "duracao": (1000, 1500),
      "pixels": [
        (255, 255, 0),
        (0, 0, 0),
        (255, 255, 0),
        (0, 0, 0),
        (255, 255, 0)
      ]
    }
  ]
}
print(f"\n Playlist: '{playlist['titulo']}'")
print(f" Total de frames: {len(playlist['frames'])}\n")



# OPERAÇÃO 1: .insert() — Adiciona um frame especial no início da playlist.

# .insert(índice, valor) coloca o elemento na posição especificada

# e empurra tudo que estava lá pra frente.

frame_intro = {
  "id": 0,
  "duracao": (0, 200), # tupla: frame curtinho de introdução
  "pixels": [(128, 128, 0)] * 5 # tudo meio apagado — fade in

}
playlist["frames"].insert(0, frame_intro) # .insert() → método de lista
print(" [INSERT] Frame de introdução adicionado no índice 0.")

# OPERAÇÃO 2: .pop() — Remove o último frame (o repete era desnecessário).

# .pop() sem argumento remove e retorna o último elemento da lista.

removido = playlist["frames"].pop() # .pop() → método de lista
print(f" [POP] Frame id={removido['id']} removido do final da playlist.")

# Agora vamos percorrer a estrutura completa com 3 níveis de for

# e calcular a duração total da animação no processo.

print(f"\n Frames da playlist '{playlist['titulo']}':\n")

duracao_total_ms = 0

# Nível 1: itera sobre as chaves do dicionário playlist usando .keys()

for chave in playlist.keys(): # .keys() → método de dicionário
  if chave == "frames":
    # Nível 2: itera sobre cada frame (que é um dicionário) da lista.

    for frame in playlist[chave]:
      inicio, fim = frame["duracao"] # desempacota a tupla de duração
      duracao_frame = fim - inicio
      duracao_total_ms += duracao_frame

      print(f" Frame #{frame['id']} | Duração: {inicio}ms → {fim}ms ({duracao_frame}ms)")
      print(f"  Pixels:", end=" ")

      # Nível 3: itera sobre cada pixel (tupla RGB) dentro do frame.

      # Aqui checa se o pixel é amarelo ou preto e imprime um símbolo.

      for pixel in frame["pixels"]:
        r, g, b = pixel # desempacota a tupla
        if r > 0 and g > 0:
          print("🟡", end="")
        else:
          print("⬛", end="")
      print() # quebra de linha depois dos pixels

print(f"\n ✅ Duração total da animação: {duracao_total_ms}ms ({duracao_total_ms/1000:.1f}s)")
print(f" ✅ Total de frames na playlist final: {len(playlist['frames'])}")


