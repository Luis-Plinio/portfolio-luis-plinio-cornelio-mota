# DESAFIO 3: O INTEGRADOR — PLAYLIST DE IMAGENS

# Aqui a missão é criar uma estrutura que mistura tudo:

# dicionário, lista e tupla juntos.

# Uso 3 níveis de for e pelo menos um método de cada estrutura.

# Métodos usados: .pop() (lista), .keys() (dict), .insert() (lista)

