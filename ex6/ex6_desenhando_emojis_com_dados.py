# Estrutura base fornecida pelo professor.

# "Toques" é uma lista de dicionários, onde cada dicionário tem

# o nome da música como chave e uma tupla de listas como valor.

biblioteca_musical = {
  "Toques": [
    {"Alegre": ([440, 480], [520, 560])},
    {"Triste": ([200, 150], [100, 50])}
  ]
}

# Vou guardar as matrizes transpostas numa estrutura nova,

# porque mexer no original enquanto itera é receita pra bug.

biblioteca_transposta = {"Toques": []}

print("\nMatrizes ORIGINAIS e suas TRANSPOSIÇÕES:\n")

# Primeiro nível: itera sobre a lista de "Toques".

# Cada item dessa lista é um dicionário com uma música.

for musica_dict in biblioteca_musical["Toques"]:

  novo_dict = {} # vai guardar a versão transposta dessa música

  # Segundo nível: itera sobre os itens do dicionário da música.

  # .items() devolve (nome_musica, (linha0, linha1)).

  for nome, matriz in musica_dict.items():
    print(f" 🎵 Música: {nome}")
    print(f"  Original : {list(matriz)}")

    # Aqui começo a montar a matriz transposta.

    # Pra uma matriz 2x2, a transposição funciona assim:

    # transposta[0] = [matriz[0][0], matriz[1][0]]

    # transposta[1] = [matriz[0][1], matriz[1][1]]

    # Em outras palavras: transposta[coluna][linha] = original[linha][coluna]

    num_linhas = len(matriz)
    num_colunas = len(matriz[0])
    transposta = []

    # Terceiro nível: monta cada linha da transposta.

    # Cada "linha da transposta" é na verdade uma coluna do original.

    for col in range(num_colunas):
      nova_linha = []
      for linha in range(num_linhas):

        # Aqui pega o elemento na posição (linha, col) do original

        # e joga na posição (col, linha) da transposta.

        nova_linha.append(matriz[linha][col])
      transposta.append(nova_linha)

    print(f"  Transposta : {transposta}\n")

    # Atualiza o dicionário com a nova matriz usando .update().

    # O .update() substitui o valor da chave se já existir,

    # ou adiciona um novo par chave-valor se não existir.

    # Aqui uso pra colocar a matriz transposta no lugar da original.

    novo_dict.update({nome: transposta})

  biblioteca_transposta["Toques"].append(novo_dict)

print(" Estrutura transposta completa:")
print(f" {biblioteca_transposta}")





